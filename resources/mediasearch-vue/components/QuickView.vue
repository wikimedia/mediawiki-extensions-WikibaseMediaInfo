<template>
	<!-- eslint-disable vue/no-v-html -->
	<div class="wbmi-media-search-quick-view" :class="rootClasses">
		<header class="wbmi-media-search-quick-view__header">
			<img v-if="isBitmap"
				:srcset="srcset"
				:sizes="sizes"
				:src="thumbnail"
				:alt="title"
				class="wbmi-media-search-quick-view__thumbnail
					wbmi-media-search-quick-view__thumbnail--image">

			<video v-else-if="isVideo"
				controls
				class="wbmi-media-search-quick-view__thumbnail
					wbmi-media-search-quick-view__thumbnail--video">

				<source
					:src="imageinfo[ 0 ].url"
					:type="mimeType"
				>
			</video>

			<audio v-else-if="isAudio"
				controls
				class="wbmi-media-search-quick-view__thumbnail
					wbmi-media-search-quick-view__thumbnail--audio">

				<source
					:src="imageinfo[ 0 ].url"
					:type="mimeType"
				>
			</audio>

			<a ref="close"
				tabindex="0"
				class="wbmi-media-search-quick-view__close-button"
				role="button"
				@keyup.enter="closeAndRestoreFocus"
				@click="close"
			>
				<wbmi-icon :icon="icons.wbmiIconClose"></wbmi-icon>
				<span class="wbmi-media-search-quick-view__close-button-text">
					{{ closeButtonText }}
				</span>
			</a>
		</header>

		<!-- File details: most of this information comes from the Commons
		Metadata API; the data available for a given file can vary widely and
		may include complex HTML generated by templates. -->
		<div class="wbmi-media-search-quick-view__body">
			<h3 class="wbmi-media-search-quick-view__title">
				<a ref="title"
					:href="canonicalurl"
					:title="title"
					target="blank">
					{{ title }}
				</a>
			</h3>

			<p v-if="description"
				class="wbmi-media-search-quick-view__description"
				v-html="description">
			</p>

			<p v-if="artist">
				<wbmi-icon :icon="icons.wbmiIconUserAvatar"></wbmi-icon>
				<span v-html="artist"></span>
			</p>

			<!-- Attempt to show license text, an appropriate icon, and an
			optional link to external license URL -->
			<p v-if="licenseText">
				<wbmi-icon v-if="licenseIcon" :icon="licenseIcon"></wbmi-icon>
				<a v-if="licenseUrl"
					:href="licenseUrl"
					target="_blank">
					<span v-html="licenseText"></span>
				</a>
				<span v-else v-html="licenseText"></span>
			</p>

			<!-- Sometimes this is free text, sometimes it is formatted. Can
			we make things semi-consistent? -->
			<p v-if="creationDate">
				<wbmi-icon :icon="icons.wbmiIconClock"></wbmi-icon>
				<span v-html="creationDate"></span>
			</p>

			<p v-if="resolution">
				<wbmi-icon :icon="icons.wbmiIconCamera"></wbmi-icon>
				<span>{{ resolution }}</span>
			</p>

			<p v-if="mimeType">
				<wbmi-icon :icon="icons.wbmiIconPageSettings"></wbmi-icon>
				<span>{{ mimeType }}</span>
			</p>

			<a :href="canonicalurl"
				:title="title"
				target="_blank"
				class="wbmi-media-search-quick-view__cta">
				{{ $i18n( 'wikibasemediainfo-special-mediasearch-quickview-button-text' ) }}
			</a>
		</div>
	</div>
</template>

<script>
var WbmiIcon = require( './base/Icon.vue' ),
	icons = require( '../../../lib/icons.js' ),
	PREVIEW_SIZES = [ 640, 800, 1200, 1600 ], // Pre-defined set of thumbnail image width values
	MAX_SIZE = 2000;

// Helper function to check for date validity
function isValidDate( d ) {
	return d instanceof Date && !isNaN( d );
}

/**
 * @file QuickView.vue
 *
 * Component to display expanded details about a given search result
 */
// @vue/component
module.exports = {
	name: 'QuickView',

	components: {
		'wbmi-icon': WbmiIcon
	},

	props: {
		title: {
			type: String,
			required: true
		},

		canonicalurl: {
			type: String,
			required: true
		},

		pageid: {
			type: Number,
			required: true
		},

		imageinfo: {
			type: Array,
			required: false,
			default: function () {
				return [ {} ];
			}
		},

		mediaType: {
			type: String,
			required: false,
			default: 'bitmap'
		},

		isDialog: {
			type: Boolean
		}
	},

	data: function () {
		return {
			icons: icons,
			closeButtonText: this.$i18n( 'wikibasemediainfo-special-mediasearch-quickview-close-button-text' )
		};
	},

	computed: {
		rootClasses: function () {
			return {
				'wbmi-media-search-quick-view--bitmap': this.isBitmap,
				'wbmi-media-search-quick-view--audio': this.isAudio,
				'wbmi-media-search-quick-view--video': this.isVideo,
				'wbmi-media-search-quick-view--dialog': this.isDialog
			};
		},

		isBitmap: function () {
			return this.mediaType === 'bitmap';
		},

		isVideo: function () {
			return this.mediaType === 'video';
		},

		isAudio: function () {
			return this.mediaType === 'audio';
		},

		/**
		 * @return {string|undefined}
		 */
		thumbnail: function () {
			return this.imageinfo[ 0 ].thumburl;
		},

		/**
		 * Progressively larger thumbnail images, generated with mw.util.parseImageUrl
		 * https://doc.wikimedia.org/mediawiki-core/master/js/#!/api/mw.util-method-parseImageUrl
		 *
		 * @return {string}
		 */
		srcset: function () {
			var attributeString = '';

			if ( this.isDialog ) {
				// For Dialog-mode quickview, the image can theoretically become
				// any size (some users enable mobile skin even when using large
				// desktop monitors). So we should generate a series of image
				// URLs to attempt to accommodate most sizes and concatenate a
				// string to use as the attribute value.
				PREVIEW_SIZES.forEach( function ( size ) {
					var url = mw.util.parseImageUrl( this.thumbnail ).resizeUrl( size );
					attributeString += url + ' ' + size + 'w,\n';
				}.bind( this ) );
				// Add one final item to the list representing the maximum size.
				// If the underlying image file is smaller than the requested size,
				// parseImageUrl will simply return a link to the full-size image.
				attributeString += mw.util.parseImageUrl( this.thumbnail ).resizeUrl( MAX_SIZE ) + ' ' + MAX_SIZE + 'w';
			} else {
				// For non-dialog quickview, the element will remain confined to
				// a sidebar and will rarely be wider ~640px (would require a
				// massive screen)
				attributeString += mw.util.parseImageUrl( this.thumbnail ).resizeUrl( PREVIEW_SIZES[ 0 ] ) + ' ' + PREVIEW_SIZES[ 0 ] + 'w';
			}

			return attributeString;
		},

		/**
		 * @return {string}
		 */
		sizes: function () {
			var attributeString = '';

			if ( this.isDialog ) {
				// For dialog mode, use an image size that matches the width of
				// the viewport since the Quickview is full-width
				PREVIEW_SIZES.forEach( function ( size ) {
					attributeString += '(min-width: ' + size + 'px) ' + size + 'px,\n';
				} );
				attributeString += MAX_SIZE + 'px';
			} else {
				// For non-dialog mode, the first preview size is all that is
				// necessary
				attributeString += PREVIEW_SIZES[ 0 ] + 'px';
			}

			return attributeString;
		},

		/**
		 * @return {Object|undefined}
		 */
		metadata: function () {
			return this.imageinfo[ 0 ].extmetadata;
		},

		/**
		 * @return {string|null} String that may contain HTML
		 */
		description: function () {
			if ( this.metadata && this.metadata.ImageDescription ) {
				return this.metadata.ImageDescription.value;
			} else {
				return null;
			}
		},

		/**
		 * @return {string|null} String that may contain HTML (often a link to a User page)
		 */
		artist: function () {
			if ( this.metadata && this.metadata.Artist ) {
				return this.metadata.Artist.value;
			} else {
				return null;
			}
		},

		/**
		 * @return {string|null}
		 */
		licenseText: function () {
			if ( this.metadata && this.metadata.UsageTerms ) {
				return this.metadata.UsageTerms.value;
			} else {
				return null;
			}
		},

		licenseIcon: function () {
			if ( this.metadata && this.metadata.License ) {
				return this.getLicenseIcon( this.metadata.License.value );
			} else {
				return null;
			}
		},

		licenseUrl: function () {
			if ( this.metadata && this.metadata.LicenseUrl ) {
				return this.metadata.LicenseUrl.value;
			} else {
				return null;
			}
		},

		/**
		 * @return {string|null} String that may contain HTML
		 */
		creationDate: function () {
			var d;

			if ( this.metadata && this.metadata.DateTimeOriginal ) {
				d = new Date( this.metadata.DateTimeOriginal.value );

				// If we have a value for DateTimeOriginal at all, create a date
				// object using that value and test for validity (JS unhelpfully
				// doesn't throw an error in the Date constructor if invalid).
				// If we are dealing with a parseable date value, return a
				// consistently-formatted string.
				//
				// Otherwise just return whatever the original string was in the
				// hope that it will make sense to the user.
				if ( isValidDate( d ) ) {
					return d.toLocaleDateString( undefined, {
						day: 'numeric',
						year: 'numeric',
						month: 'long'
					} );
				} else {
					return this.metadata.DateTimeOriginal.value;
				}

			} else {
				return null;
			}
		},

		/**
		 * @return {string|null}
		 */
		resolution: function () {
			var width = this.imageinfo[ 0 ].width,
				height = this.imageinfo[ 0 ].height;

			if ( this.imageinfo && width && height ) {
				return width + ' Ã— ' + height;
			} else {
				return null;
			}
		},

		mimeType: function () {
			return this.imageinfo[ 0 ].mime;
		}
	},

	methods: {
		/**
		 * @fires close
		 */
		close: function () {
			this.$emit( 'close' );
		},

		/**
		 * Includes a boolean flag that tells the parent to restore focus to
		 * the originating search result
		 *
		 * @fires close
		 */
		closeAndRestoreFocus: function () {
			this.$emit( 'close', true );
		},

		/**
		 * Use this method if a non-HTML metadata value is required.
		 *
		 * @param {string} raw HTML string
		 * @return {string}
		 */
		stripHTML: function ( raw ) {
			return $( '<p>' ).append( raw ).text();
		},

		getLicenseIcon: function ( valueString ) {
			if ( /^cc/i.test( valueString ) ) {
				return icons.wbmiIconLogoCC;
			} else if ( /^pd/i.test( valueString ) ) {
				return icons.wbmiIconUnLock;
			} else {
				return null;
			}
		},

		/**
		 * Programatically set focus on the title element; used by the parent
		 * component when the Quickview is opened.
		 */
		focus: function () {
			this.$refs.close.focus();
		}
	}
};
</script>
